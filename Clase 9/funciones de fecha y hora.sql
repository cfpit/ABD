SELECT DATEADD(DAY, 25, '2006-10-10'); -- '2006-11-04'
SELECT DATEADD(DAY, -25, '2006-10-10'); -- '2006-09-15'

-- SQL Server
SELECT DATEADD(SECOND, -3, DATEADD(MINUTE, -2, '2006-08-10 18:55:44'));
-- '2006-08-10 18:53:41'  

-- HOUR_MINUTE → DATEADD anidado
SELECT DATEADD(MINUTE, -30, DATEADD(HOUR, -1, '2006-08-10 18:55:44'));

-- O todo en minutos: (1h * 60) + 30 = 90 min
SELECT DATEADD(MINUTE, -90, '2006-08-10 18:55:44');


SELECT DATEDIFF(DAY, '2006-01-01', '2006-12-31');  -- 364 
SELECT DATEDIFF(DAY,    '2006-01-01', '2006-12-31');  -- 364  días
SELECT DATEDIFF(MONTH,  '2006-01-01', '2006-12-31');  -- 11   meses
SELECT DATEDIFF(YEAR,   '2000-01-01', '2026-01-01');  -- 26   años
SELECT DATEDIFF(HOUR,   '2026-05-08 08:00', '2026-05-08 18:00');  -- 10 horas
SELECT DATEDIFF(MINUTE, '2026-05-08 08:00', '2026-05-08 08:45');  -- 45 minutos


SELECT DATENAME(WEEKDAY, '2020-09-24');  -- 'Thursday'  


-- SQL Server
SELECT DATEPART(YEAR,   '2006-10-10');           -- 2006  
SELECT DATEPART(MONTH,  '2006-10-10');           -- 10    
SELECT DATEPART(DAY,    '2006-10-10');           -- 10    
SELECT DATEPART(HOUR,   '2006-10-10 10:15:25'); -- 10
SELECT DATEPART(MINUTE, '2006-10-10 10:15:25'); -- 15

-- YEAR_MONTH no existe en SQL Server, combinar:
SELECT CAST(DATEPART(YEAR, '2006-10-10') AS VARCHAR)
     + RIGHT('0' + CAST(DATEPART(MONTH,'2006-10-10') AS VARCHAR), 2);
-- '200610'


SELECT GETDATE();         -- '2026-05-08 10:35:22.000'  
SELECT CURRENT_TIMESTAMP; -- igual (estándar SQL, funciona en ambos )
SELECT SYSDATETIME();     -- '2026-05-08 10:35:22.1234567' (mayor precisión)


-- SQL Server
SELECT CAST(DATEADD(SECOND, 90, '00:00:00') AS TIME);     -- '00:01:30'  
SELECT CAST(DATEADD(SECOND, 3661, '00:00:00') AS TIME);   -- '01:01:01'  


SELECT DATEDIFF(SECOND, '00:00:00', '00:01:30');  -- 90   
SELECT DATEDIFF(SECOND, '00:00:00', '01:01:01');  -- 3661



SELECT YEAR('2006-08-10');   -- 2006  
SELECT MONTH('2006-08-10');  -- 8     
SELECT DAY('2006-08-10');    -- 10    

